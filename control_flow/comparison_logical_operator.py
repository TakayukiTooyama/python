# 比較演算子
a = 1
b = 1

# 等しい（==）
print(a == b) # True

# 異なる（!=）
print(a != b) # False

# 小さい（<）
print(a < b) # False

# 大きい（==）
print(a > b) # False

# 以下（<=）
print(a <= b) # True

# 以上（>=）
print(a >= b) # True

# 論理演算子
# ⭐️and
print(a > 0 and b > 0) # True

# ⭐️or
print(a > 0 or b > 0) # True

# ⭐️not
# notはこの使い方をする
is_ok = True
if not is_ok:
    print('hello')
