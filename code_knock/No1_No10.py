# 3.文字列代入
# {}.format()
print('私は{}です。{}出身です。'.format('遠山', '新潟'))
# 私は遠山です。新潟出身です。

# f-string
name = '遠山'
birthplace = '新潟'
print(f'私は{name}です。{birthplace}出身です。')
# 私は遠山です。新潟出身です。


